//create the main function
fun main(){
    //Kotlin print functions
    //print- output displayed on one line
    //println-output displayed on the next line
    print("Hello Kotlin")
    println("Welcome to our first class")
    println("I love Kotlin programming language")

    //variables
    //var-variable can be reassigned
    //val =varible cannot be reassigned
    var name="Kotlin"
    val year=2023
    //reassign
    name="Mobile development"
    //year=2024 //cannot be reassigned
    println(name)
    println(year)

    var height=4.5
    println(height)

    val weight=70
    println(weight)

    val letter='F'
    println(letter)
}